package assignment;

public class Star_pat1 {

	public static void main(String[] args) {

		
		
		for(int i=1;i<=5;i++)
		{
			for(int k=1;k<=i;k++)
			{
				System.out.print("*");
			}
			System.out.println();
				
		}
		
	
		
		for(int k=4;k>=1;k--)
		{
			for(int l=1;l<=k;l++)
			{
				System.out.print("*");
			}
			
			System.out.println();
		}
		
		
		
		
		
		
		
		
	}
	

}
